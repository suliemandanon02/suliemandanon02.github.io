<?php
// Function to concatenate two strings
function concatenateStrings($str1, $str2) {
    return $str1 . " " . $str2;
}

// Define two strings
$string1 = "Hello";
$string2 = "world!";

// Call the function and store the result
$result = concatenateStrings($string1, $string2);

// Output the result
echo $result;
?>
